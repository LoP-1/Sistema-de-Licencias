    private void loginBtnTxt3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnTxt3MouseClicked
        
        String dni = DNIBase.getText();
        String nombre = NombreBase.getText();
        String contrasena = ContrasenaBase.getText();

        // Solicitar y guardar la contraseña
        String contrasenaBase = JOptionPane.showInputDialog(this, "Repita la contraseña:");

         if (!contrasena.equals(contrasenaBase)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden");
            return;
        }
        
         try (BufferedWriter writer = new BufferedWriter(new FileWriter("usuarios.txt", true))) {
            writer.write(dni + "," + nombre + "," +  contrasena);
            writer.newLine();
            JOptionPane.showMessageDialog(this, "Registro exitoso");
            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar los datos: " + e.getMessage());
        }
        
        
    }//GEN-LAST:event_loginBtnTxt3MouseClicked

