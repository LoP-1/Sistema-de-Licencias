    private void loginBtnTxt1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnTxt1MouseClicked
        //Login invocando al archivo usuarios TXT
        String dni = Usertxt.getText();
        String contrasena = new String(Passtxt.getPassword());

        // Si los campos están vacíos envía mensaje
        if (dni.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ambos campos son obligatorios");
            return;
        }

        try (BufferedReader reader = new BufferedReader(new FileReader("usuarios.txt"))) {
            String linea;
            boolean encontrado = false;

            while ((linea = reader.readLine()) != null) {
                String[] partes = linea.split(",");
                if (partes[1].equals(dni) && partes[2].equals(contrasena)) {
                    String mensajeBienvenida = "Bienvenido " + partes[1] + " " + partes[2];
                    JOptionPane.showMessageDialog(null, mensajeBienvenida);
                    new Principal().setVisible(true);
                    this.dispose();
                    encontrado = true;
                    break;
                }
            }

            if (!encontrado) {
                intentosFallidos++;
                if (intentosFallidos >= 3) {
                    JOptionPane.showMessageDialog(null, "Demasiados intentos fallidos. Cerrando la aplicación.");
                    System.exit(0);
                } else {
                    JOptionPane.showMessageDialog(null, "DNI o contraseña incorrecta. Intento fallido número: " + intentosFallidos);
                    Usertxt.setText("");
                    Passtxt.setText("");
                }
            }

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error al leer los datos: " + e.getMessage());
        }
    }//GEN-LAST:event_loginBtnTxt1MouseClicked

