    private void Exit4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Exit4ActionPerformed

         pdf obj = new pdf();
        
            Font fontTexto = new Font(Font.HELVETICA, 8, Font.NORMAL, Color.BLACK);
            Paragraph texto1 = new Paragraph("\n", fontTexto);

        
            texto1.add("\n" + arbolReporte.contarEstablecimientos() + "\n");
            texto1.add(arbolReporte.contarGiroActividad() + "\n");
            texto1.add(arbolReporte.contarTipoLicencia() + "\n");
            obj.ReportePDF(texto1,"Reporte Busquedas");
        
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileFilter(new FileNameExtensionFilter("Archivos de texto", "txt"));
            int returnValue = fileChooser.showSaveDialog(null);

            if (returnValue == JFileChooser.APPROVE_OPTION) {
                File file = fileChooser.getSelectedFile();
                String filePath = file.getAbsolutePath();
                if (!filePath.endsWith(".txt")) {
                    filePath += ".txt";
                }

                try (FileWriter writer = new FileWriter(filePath)) {
                    
                    for (Nodo nodo : resultados) {
                        writer.write(nodo.ID + "," + nodo.ruc + "," + nodo.giroActividad + "," + nodo.areaEstablecimiento + "," + nodo.tipoLicencia + "\n");
                    }
                    System.out.println("Exportación a archivo de texto completada.");
                } catch (IOException e) {
                    e.printStackTrace();
                }
            } else {
                System.out.println("No se seleccionó ningún archivo para guardar.");
            }
    }//GEN-LAST:event_Exit4ActionPerformed

