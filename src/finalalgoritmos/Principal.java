    private void bt_importar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_importar1MouseClicked
        //Codigo para importar
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Archivos CSV", "csv"));

        // Mostrar el diálogo de abrir archivo
        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String filePath = selectedFile.getAbsolutePath();

            // Procesar el archivo seleccionado
            try (CSVReader reader = new CSVReader(new FileReader(filePath))) {
                List<String[]> records = reader.readAll();
                for (String[] record : records) {
                    if (record.length >= 4) {
                        String ruc = record[0].trim();
                        String giroDeActividad = record[1].trim();
                        String areaDeEstablecimiento = record[2].trim();
                        String tipoDeLicencia = record[3].trim();

                        licencias.insertar(ID, ruc, giroDeActividad, areaDeEstablecimiento, tipoDeLicencia);
                        arbolPadre.insertar(ruc, giroDeActividad, areaDeEstablecimiento, tipoDeLicencia);
                        ID++;
                    } else {
                        System.err.println("Registro inválido: " + String.join(",", record));
                    }
                }
            } catch (IOException | CsvException e) {
                e.printStackTrace();
            }

            // Llenar la tabla después de insertar los datos
            llenarTabla();
        } else {
            System.out.println("No se seleccionó ningún archivo.");
        }
        
    }//GEN-LAST:event_bt_importar1MouseClicked

